print("hello\nworld")
"""
This script demonstrates the usage of the `print` function in Python with different parameters:

1. A simple print statement with a newline character (`\n`) to display text on separate lines.
2. A print statement using the `sep` parameter to specify a custom separator (`>>`) between multiple arguments.
3. A print statement using the `end` parameter to specify a custom string (`>>\n`) to be appended at the end of the output instead of the default newline.

"""

print(1, 2,3,4, sep=">>")
print("1", "2", "3", "4", end=">>\n")
print("1", "2", "3", "4", end=">>\n")
print("1", "2", "3", "4", end=">>\n")